# Virtual Enviroments

# Un entorno virtual es un
# entorno Python en el que
# el intérprete Python, las
# bibliotecas y los scripts
# instalados en él están
# aislados de los instalados
# en otros entornos virtuales,
# y (por defecto) cualquier
# biblioteca instalada en un
# sistema Python, es decir,
# uno que esté instalado como
# parte de tu sistema operativo

# comando para crear uno:
# Windows:
# py -m venv <nombre>
# Linux:
# python3 -m venv <nombre>

# comando para activarlo:
# Windows:
# .\<nombre>\Scripts\activate
# Linux:
# source <nombre>/bin/activate

# cómo intalar librerías:
# Windows y Linux:
# pip install <librería>